package Snake.game;

public class mela {
    private int posMelaX;
    private int posMelaY;

    public mela(){

    }

    public void setPosY(int posMelaY) {
        this.posMelaY=posMelaY;
    }

    public void setPosX(int posMelaX) {
        this.posMelaX=posMelaX;
    }

    public int getPosX() {
        return posMelaX;
    }
    public int getPosY() {
        return posMelaY;
    }

}
